#include "libft.h"

size_t	ft_intlen(long int n)
{
	int i;

	i = (n < 0) ? 1 : 0;
	n = (n < 0) ? -n : n;
	while (n > 9)
	{
		n /= 10;
		i++;
	}
	return (i + 1);
}
