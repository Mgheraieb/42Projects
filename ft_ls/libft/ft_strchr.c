#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!(*s) || !s)
		return (NULL);
	while (*s || *s == c)
	{
		if (*s == c)
			return ((char*)s);
		s++;
	}
	return (NULL);
}
